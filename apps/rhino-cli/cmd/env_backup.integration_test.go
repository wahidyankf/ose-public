func (s *envBackupIntSteps) aGitRepositoryContainingSymlinkOversizedAndRegularEnvFile() error {
	repo, err := s.makeRepo("int-env-backup-mixed-*")
	if err != nil {
		return err
	}
	s.repoDir = repo

	// Regular file.
	if err := writeEnvFile(repo, ".env", "REGULAR=1\n"); err != nil {
		return err
	}

	// Symlink .env.symlink → .env.
	target := filepath.Join(repo, ".env")
	link := filepath.Join(repo, ".env.symlink")
	if err := os.Symlink(target, link); err != nil {
		return fmt.Errorf("create symlink: %w", err)
	}

	// Oversized file (>1 MB).
	bigContent := strings.Repeat("x", 1024*1024+1)
	if err := writeEnvFile(repo, ".env.big", bigContent); err != nil {
		return err
	}

	backup, err := s.makeBackupDir("int-env-backup-dest-*")
	if err != nil {
		return err
	}
	s.backupDir = backup
	envBackupDir = backup
	return os.Chdir(repo)
}

