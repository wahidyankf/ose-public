type envBackupIntSteps struct {
	originalWd string
	repoDir    string // git root (real files on disk)
	backupDir  string // backup destination
	cmdErr     error
	cmdOutput  string
}

func (s *envBackupIntSteps) before(_ context.Context, _ *godog.Scenario) (context.Context, error) {
	s.originalWd, _ = os.Getwd()
	s.repoDir = ""
	s.backupDir = ""
	s.cmdErr = nil
	s.cmdOutput = ""
	verbose = false
	quiet = false
	output = "text"
	envBackupDir = ""
	envBackupWorktreeAware = false
	envBackupFn = envbackup.Backup
	// Restore real OS functions.
	osGetwd = os.Getwd
	osStat = os.Stat
	return context.Background(), nil
}

func (s *envBackupIntSteps) after(_ context.Context, _ *godog.Scenario, _ error) (context.Context, error) {
	_ = os.Chdir(s.originalWd)
	envBackupDir = ""
	envBackupWorktreeAware = false
	envBackupFn = envbackup.Backup
	confirmFn = envbackup.DefaultConfirmFn
	osGetwd = os.Getwd
	osStat = os.Stat
	envBackupForce = false
	envBackupIncludeConfig = false
	if s.repoDir != "" {
		_ = os.RemoveAll(s.repoDir)
	}
	if s.backupDir != "" {
		_ = os.RemoveAll(s.backupDir)
	}
	return context.Background(), nil
}

// makeRepo creates a temp dir with a .git directory (simulating a repo root).
func (s *envBackupIntSteps) makeRepo(pattern string) (string, error) {
	dir, err := os.MkdirTemp("", pattern)
	if err != nil {
		return "", fmt.Errorf("create repo dir: %w", err)
	}
	if err := os.MkdirAll(filepath.Join(dir, ".git"), 0o755); err != nil {
		return "", fmt.Errorf("create .git dir: %w", err)
	}
	return dir, nil
}

// makeBackupDir creates a temp dir outside the repo.
func (s *envBackupIntSteps) makeBackupDir(pattern string) (string, error) {
	dir, err := os.MkdirTemp("", pattern)
	if err != nil {
		return "", fmt.Errorf("create backup dir: %w", err)
	}
	return dir, nil
}

// writeFile creates a file at relPath under dir with the given content.
func writeEnvFile(dir, relPath, content string) error {
	full := filepath.Join(dir, relPath)
	if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
		return fmt.Errorf("mkdir for %s: %w", relPath, err)
	}
	return os.WriteFile(full, []byte(content), 0o644)
}

