// TestEnvBackupCmd_FnError verifies that errors from envBackupFn are propagated.
func TestEnvBackupCmd_FnError(t *testing.T) {
	original := envBackupFn
	defer func() { envBackupFn = original }()

	origGetwd := osGetwd
	origStat := osStat
	defer func() {
		osGetwd = origGetwd
		osStat = origStat
	}()

	osGetwd = func() (string, error) { return "/mock-repo", nil }
	osStat = func(name string) (os.FileInfo, error) {
		if name == "/mock-repo/.git" {
			return &mockFileInfo{name: ".git", isDir: true}, nil
		}
		return nil, os.ErrNotExist
	}

	envBackupFn = func(_ envbackup.Options) (*envbackup.Result, error) {
		return nil, fmt.Errorf("simulated backup failure")
	}

	origDir := envBackupDir
	defer func() { envBackupDir = origDir }()

	tmpBackup, err := os.MkdirTemp("", "env-backup-err-*")
	if err != nil {
		t.Fatalf("create temp dir: %v", err)
	}
	defer func() { _ = os.RemoveAll(tmpBackup) }()
	envBackupDir = tmpBackup

	buf := new(bytes.Buffer)
	envBackupCmd.SetOut(buf)
	envBackupCmd.SetErr(buf)
	cmdErr := envBackupCmd.RunE(envBackupCmd, []string{})
	if cmdErr == nil {
		t.Errorf("expected error from envBackupFn to propagate, got nil")
	}
}
