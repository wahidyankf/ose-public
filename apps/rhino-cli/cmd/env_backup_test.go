type envBackupUnitSteps struct {
	cmdErr    error
	cmdOutput string
	backupDir string // temp dir used as backup destination
	repoDir   string // temp dir used as fake repo root
}

func (s *envBackupUnitSteps) before(_ context.Context, _ *godog.Scenario) (context.Context, error) {
	verbose = false
	quiet = false
	output = "text"
	envBackupDir = ""
	envBackupWorktreeAware = false
	envBackupForce = false
	envBackupIncludeConfig = false
	s.cmdErr = nil
	s.cmdOutput = ""
	s.backupDir = ""
	s.repoDir = ""

	// Mock findGitRoot via osGetwd/osStat — default to /mock-repo.
	osGetwd = func() (string, error) { return "/mock-repo", nil }
	osStat = func(name string) (os.FileInfo, error) {
		if name == "/mock-repo/.git" {
			return &mockFileInfo{name: ".git", isDir: true}, nil
		}
		return nil, os.ErrNotExist
	}

	// Default mock: backup succeeds with 2 copied files.
	envBackupFn = func(opts envbackup.Options) (*envbackup.Result, error) {
		return &envbackup.Result{
			Direction: "backup",
			Dir:       opts.BackupDir,
			Files: []envbackup.FileEntry{
				{RelPath: ".env", AbsPath: "/mock-repo/.env", Size: 100},
				{RelPath: "apps/web/.env", AbsPath: "/mock-repo/apps/web/.env", Size: 200},
			},
			Copied:  2,
			Skipped: 0,
		}, nil
	}

	return context.Background(), nil
}

func (s *envBackupUnitSteps) after(_ context.Context, _ *godog.Scenario, _ error) (context.Context, error) {
	envBackupFn = envbackup.Backup
	confirmFn = envbackup.DefaultConfirmFn
	osGetwd = os.Getwd
	osStat = os.Stat
	envBackupForce = false
	envBackupIncludeConfig = false
	if s.backupDir != "" {
		_ = os.RemoveAll(s.backupDir)
	}
	if s.repoDir != "" {
		_ = os.RemoveAll(s.repoDir)
	}
	return context.Background(), nil
}

