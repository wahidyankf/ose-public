func (s *envBackupUnitSteps) aGitWorktreeNamedFeatureBranchWithEnvFile() error {
	// Create a temp dir simulating a worktree (dir-name is "feature-branch").
	tmpRepo, err := os.MkdirTemp("", "feature-branch-*")
	if err != nil {
		return fmt.Errorf("create temp worktree dir: %w", err)
	}
	// Rename so basename is "feature-branch".
	featureBranchDir := filepath.Join(filepath.Dir(tmpRepo), "feature-branch")
	if err := os.Rename(tmpRepo, featureBranchDir); err != nil {
		// If rename fails (cross-device), just keep tmpRepo.
		featureBranchDir = tmpRepo
	}
	s.repoDir = featureBranchDir

	// Create .git as a file to simulate a linked worktree.
	if err := os.WriteFile(filepath.Join(featureBranchDir, ".git"), []byte("gitdir: /some/real/.git\n"), 0o644); err != nil {
		return fmt.Errorf("write .git file: %w", err)
	}

	osGetwd = func() (string, error) { return featureBranchDir, nil }
	osStat = func(name string) (os.FileInfo, error) {
		if name == filepath.Join(featureBranchDir, ".git") {
			return &mockFileInfo{name: ".git", isDir: false}, nil
		}
		return nil, os.ErrNotExist
	}

	envBackupFn = func(opts envbackup.Options) (*envbackup.Result, error) {
		return &envbackup.Result{
			Direction:    "backup",
			Dir:          opts.BackupDir,
			WorktreeName: "feature-branch",
			Files: []envbackup.FileEntry{
				{RelPath: ".env", AbsPath: filepath.Join(featureBranchDir, ".env"), Size: 100},
			},
			Copied:  1,
			Skipped: 0,
		}, nil
	}
	return nil
}

